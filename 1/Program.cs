double Factorial(int n) //double для вещественных чисел
{
    if(n == 1)
    return 1;
    else
    return n * Factorial(n - 1);
}
for (int i = 1; i < 40; i++)
{
    Console.WriteLine($"{i}! = {Factorial(i)}");
}

//фибоначчи
double Fibonacci(int n)
{
    if(n == 1 || n == 2)
    return 1;
    else
    return Fibonacci(n - 1) + Fibonacci(n - 2);
}
for (int i = 1; i <= 50; i++)
{
    Console.WriteLine($"{i}! = {Fibonacci(i)}");
}


